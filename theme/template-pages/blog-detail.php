                            <h2 class="wp-block-heading"><span class="ez-toc-section"
                                    id="chinh-sach-mien-thi-thuc-khi-nhap-canh-vao-jeju"></span>Chính sách miễn thị thực
                                khi nhập cảnh vào Jeju<span class="ez-toc-section-end"></span></h2>



                            <p>Khác với các khu vực khác ở Hàn Quốc yêu cầu visa nhập cảnh, Jeju mở cửa chào đón du
                                khách từ một số quốc gia nhất định mà không cần xin visa, mang đến trải nghiệm du lịch
                                hoàn hảo và tiện lợi. Nhờ chính sách này, du khách có thể dễ dàng vượt qua cửa an ninh,
                                tiết kiệm thời gian và thủ tục, từ đó thỏa sức khám phá vẻ đẹp thiên nhiên và văn hóa
                                độc đáo của hòn đảo.</p>



                            <p>Chính phủ Hàn Quốc triển khai chương trình miễn thị thực du lịch Jeju 30 ngày cho du
                                khách đến từ nhiều quốc gia, bao gồm cả khu vực Đông Nam Á. Nhờ chính sách này, du khách
                                Việt Nam có thể thuận tiện di chuyển đến và rời khỏi đảo Jeju mà không cần trải qua thủ
                                tục xin visa rườm rà.</p>



                            <h2 class="wp-block-heading"><span class="ez-toc-section"
                                    id="dieu-kien-de-du-lich-dao-jeju-khong-can-visa"></span>Điều kiện để du lịch đảo
                                Jeju không cần visa<span class="ez-toc-section-end"></span></h2>



                            <p>để được hưởng chính sách này, công dân Việt Nam cần đáp ứng một số điều kiện cụ thể. Dưới
                                đây là các điều kiện chi tiết:</p>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="muc-dich-nhap-canh-chi-gioi-han-o-dao-jeju"></span>Mục đích nhập cảnh chỉ giới
                                hạn ở đảo Jeju<span class="ez-toc-section-end"></span></h3>



                            <p>Du khách phải có mục đích nhập cảnh là du lịch, nghỉ dưỡng hoặc thăm thân nhân, và chỉ
                                lưu trú trong phạm vi đảo Jeju. Chính sách miễn visa này không cho phép di chuyển đến
                                các khu vực khác của Hàn Quốc (như Seoul, Busan, hay Incheon). Nếu bạn có ý định rời đảo
                                Jeju để khám phá đất liền, bạn sẽ cần xin visa Hàn Quốc thông thường.</p>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="thoi-gian-luu-tru-toi-da-30-ngay"></span>Thời gian lưu trú tối đa 30 ngày<span
                                    class="ez-toc-section-end"></span></h3>



                            <p>Công dân Việt Nam được phép lưu trú tại đảo Jeju mà không cần visa trong tối đa 30 ngày
                                kể từ ngày nhập cảnh. Nếu bạn dự định ở lại lâu hơn hoặc có kế hoạch khác ngoài du lịch,
                                chính sách miễn visa sẽ không áp dụng.</p>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="nhap-canh-truc-tiep-vao-dao-jeju"></span>Nhập cảnh trực tiếp vào đảo Jeju<span
                                    class="ez-toc-section-end"></span></h3>



                            <p>Để được miễn visa, bạn phải nhập cảnh trực tiếp vào đảo Jeju thông qua Sân bay Quốc tế
                                Jeju (mã sân bay: CJU) hoặc cảng biển Jeju bằng đường hàng không hoặc đường biển. Tuy
                                nhiên, hiện tại Việt Nam chưa có đường bay thẳng từ các thành phố lớn như Hà Nội hay
                                TP.HCM đến Jeju.</p>



                            <p>&nbsp;Do đó, du khách thường phải quá cảnh tại một quốc gia thứ ba (ví dụ: Singapore,
                                Thái Lan) hoặc thông qua các sân bay Hàn Quốc như Incheon hoặc Gimpo, nhưng với điều
                                kiện đặc biệt (xem mục tiếp theo).</p>



                            <h3 class="wp-block-heading"><span class="ez-toc-section" id="qua-canh-phu-hop"></span>Quá
                                cảnh phù hợp<span class="ez-toc-section-end"></span></h3>



                            <ul>
                                <li><strong>Quá cảnh tại một quốc gia thứ ba</strong>: Vì không có chuyến bay thẳng từ
                                    Việt Nam đến Jeju, một cách phổ biến là bay từ Việt Nam đến một quốc gia thứ ba (như
                                    Singapore hoặc Thái Lan), sau đó nối chuyến trực tiếp đến Jeju. Ví dụ, bạn có thể
                                    bay từ TP.HCM đến Singapore (miễn visa cho người Việt trong 30 ngày), rồi từ
                                    Singapore bay thẳng đến Jeju bằng các hãng hàng không như Scoot Airlines. Điều này
                                    giúp bạn tránh quá cảnh tại các sân bay Hàn Quốc khác, vốn có thể yêu cầu visa.</li>



                                <li><strong>Quá cảnh tại Hàn Quốc</strong>: Nếu bạn chọn quá cảnh tại các sân bay lớn
                                    của Hàn Quốc như Incheon (ICN) hoặc Gimpo (GMP) trước khi đến Jeju, chính sách miễn
                                    visa vẫn có thể áp dụng, nhưng chỉ khi bạn tham gia các tour du lịch được chỉ định
                                    (xem mục 5). Trong trường hợp tự túc, bạn sẽ cần visa Hàn Quốc để quá cảnh hợp pháp
                                    tại các sân bay này.</li>
                            </ul>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="tham-gia-tour-du-lich-duoc-chi-dinh-neu-qua-canh-tai-han-quoc"></span>Tham gia
                                tour du lịch được chỉ định (nếu quá cảnh tại Hàn Quốc)<span
                                    class="ez-toc-section-end"></span></h3>



                            <ul>
                                <li>Nếu bạn bay từ Việt Nam, quá cảnh tại sân bay Incheon hoặc Gimpo, rồi tiếp tục đến
                                    Jeju, bạn có thể được miễn visa nếu tham gia các tour du lịch do các công ty lữ hành
                                    được Hàn Quốc cấp phép tổ chức (gọi là “tour free-visa”). Các điều kiện cụ thể bao
                                    gồm:
                                    <ul>
                                        <li>Phải cung cấp lịch trình du lịch rõ ràng, trong đó điểm đến duy nhất là đảo
                                            Jeju.</li>



                                        <li>Công ty du lịch tổ chức tour phải nằm trong danh sách được Bộ Tư pháp Hàn
                                            Quốc hoặc Cục Quản lý Xuất nhập cảnh Hàn Quốc phê duyệt.</li>



                                        <li>Bạn không được phép rời đoàn hoặc tự ý di chuyển ra ngoài đảo Jeju.</li>
                                    </ul>
                                </li>



                                <li>Đây là lựa chọn phổ biến và an toàn cho những ai muốn đơn giản hóa thủ tục, vì công
                                    ty du lịch sẽ lo toàn bộ giấy tờ và đảm bảo tuân thủ quy định nhập cảnh.</li>
                            </ul>



                            <h3 class="wp-block-heading"><span class="ez-toc-section" id="ho-chieu-hop-le"></span>Hộ
                                chiếu hợp lệ<span class="ez-toc-section-end"></span></h3>



                            <ul>
                                <li>Hộ chiếu của bạn phải là hộ chiếu phổ thông (không phải hộ chiếu công vụ hoặc ngoại
                                    giao) và còn thời hạn ít nhất 6 tháng tính từ ngày nhập cảnh vào Jeju.</li>
                            </ul>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="khai-bao-k-eta-neu-yeu-cau"></span>Khai báo K-ETA (nếu yêu cầu)<span
                                    class="ez-toc-section-end"></span></h3>



                            <p>Mặc dù chính sách miễn visa áp dụng cho Jeju, Hàn Quốc đôi khi yêu cầu du khách từ một số
                                quốc gia, bao gồm Việt Nam, phải khai báo Hệ thống Ủy quyền Du lịch Điện tử Hàn Quốc
                                (K-ETA) trước khi nhập cảnh.&nbsp;</p>



                            <p>K-ETA là một loại giấy phép điện tử nhằm kiểm soát thông tin du khách, thường có hiệu lực
                                trong 2 năm hoặc đến khi hộ chiếu hết hạn. Bạn nên kiểm tra thông tin mới nhất từ Đại sứ
                                quán Hàn Quốc hoặc trang web chính thức của K-ETA (<a
                                    href="https://www.k-eta.go.kr">https://www.k-eta.go.kr</a>) để xác nhận xem yêu cầu
                                này có áp dụng cho Jeju vào thời điểm bạn đi hay không.</p>



                            <h3 class="wp-block-heading"><span class="ez-toc-section"
                                    id="khong-co-tien-an-tien-su-hoac-vi-pham-nhap-cu"></span>Không có tiền án, tiền sự
                                hoặc vi phạm nhập cư<span class="ez-toc-section-end"></span></h3>



                            <p>Du khách không được có tiền án, tiền sự hoặc từng vi phạm các quy định về nhập cư của Hàn
                                Quốc (ví dụ: ở lại quá hạn visa trước đó). Nếu bạn nằm trong danh sách cấm nhập cảnh của
                                Hàn Quốc, chính sách miễn visa sẽ không được áp dụng.</p>



